/**
 * @brief Kumpulkan semua patch aktif dari oMap menjadi list CGPatchEntry.
 *
 * Template agar bisa menerima oMap dari ClassesTab (unordered_map<MethodInfo*, OriginalMethodBytes>)
 * tanpa circular dependency.
 *
 * @tparam OMapType   unordered_map<MethodInfo*, T>  dimana T memiliki .bytes dan .text
 * @param  oMap       Map patch aktif dari ClassesTab
 * @return std::vector<CGPatchEntry>
 */
template <typename OMapType>
inline std::vector<CGPatchEntry> CollectPatches(const OMapType& oMap) {
    std::vector<CGPatchEntry> result;
    result.reserve(oMap.size());

    const char* libName = GetTargetLib();
    uintptr_t   libBase = GetLibBase(libName);
    size_t      libSize = GetLibRegionSize(libName);

    for (const auto& [method, data] : oMap) {
        if (!method || !method->methodPointer || data.bytes.empty()) continue;

        CGPatchEntry e;
        auto* klass   = method->getClass();
        e.className   = (klass && klass->getName()) ? klass->getName() : "Unknown";
        e.methodName  = method->getName()            ? method->getName()  : "method";
        e.featureName = Sanitize(e.className) + "_" + Sanitize(e.methodName);
        e.libName     = libName;
        e.libSize     = libSize;

        uintptr_t absAddr = (uintptr_t)method->methodPointer;
        e.rva = (libBase && absAddr > libBase) ? (absAddr - libBase) : absAddr;

        auto* rt          = method->getReturnType();
        e.returnTypeName  = rt ? rt->getName() : "System.Void";
        e.bytesOriginal   = data.bytes;
        e.patchText       = data.text.empty() ? "NOP" : data.text;

        // ← Panggil SATU sumber kebenaran, bukan inline lagi
        e.bytesPatched = DerivePatchBytes(e.returnTypeName, e.patchText);

        result.push_back(std::move(e));
    }
    return result;
}

