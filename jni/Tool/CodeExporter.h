/// Build isi file AutoGenerated.h dari list patches
inline std::string BuildCppHeader(const std::vector<CGPatchEntry>& patches) {
    std::ostringstream h;

    h << "// ============================================================\n"
      << "// AutoGenerated.h — IL2CPP Tool (CodeExporter)\n"
      << "// RVA = offset relatif dari base libil2cpp.so\n"
      << "// RVA TIDAK berubah antar restart (walaupun base ASLR berubah)\n"
      << "//\n"
      << "// Cara pakai:\n"
      << "//   uintptr_t base = GetLibBase(\"libil2cpp.so\");\n"
      << "//   PatchMemory(base + RVA_XXX, bytes_on_xxx, sizeof(bytes_on_xxx));\n"
      << "// ============================================================\n"
      << "#pragma once\n"
      << "#include <cstdint>\n"
      << "#include <cstring>\n"
      << "#include <sys/mman.h>\n"
      << "#include <unistd.h>\n\n";

    // ── GetLibBase helper ────────────────────────────────────────────────────
    h << "// ── GetLibBase: cari base library via /proc/self/maps (cached) ──\n"
      << "static uintptr_t _s_libBase = 0;\n"
      << "inline uintptr_t GetLibBase(const char* libName) {\n"
      << "    if (_s_libBase) return _s_libBase;\n"
      << "    FILE* fp = fopen(\"/proc/self/maps\", \"r\"); if (!fp) return 0;\n"
      << "    char line[512];\n"
      << "    while (fgets(line, sizeof(line), fp)) {\n"
      << "        if (!strstr(line, libName)) continue;\n"
      << "        sscanf(line, \"%lx\", &_s_libBase);\n"
      << "        if (_s_libBase) break;\n"
      << "    }\n"
      << "    fclose(fp);\n"
      << "    return _s_libBase;\n"
      << "}\n\n";

    // ── PatchMemory helper ───────────────────────────────────────────────────
    h << "// ── PatchMemory: tulis bytes ke alamat memori (mprotect + memcpy) ──\n"
      << "inline bool PatchMemory(uintptr_t addr, const uint8_t* bytes, size_t len) {\n"
      << "    if (!addr || !bytes || !len) return false;\n"
      << "    size_t    ps  = (size_t)sysconf(_SC_PAGESIZE);\n"
      << "    uintptr_t pb  = addr & ~(ps - 1);\n"
      << "    if (mprotect((void*)pb, ps, PROT_READ|PROT_WRITE|PROT_EXEC) != 0) return false;\n"
      << "    memcpy((void*)addr, bytes, len);\n"
      << "    mprotect((void*)pb, ps, PROT_READ|PROT_EXEC);\n"
      << "    __builtin___clear_cache((char*)addr, (char*)addr + len);\n"
      << "    return true;\n"
      << "}\n\n";

    // ── RVA defines & byte arrays ────────────────────────────────────────────
    h << "// ── RVA & Patch Bytes ──────────────────────────────────────────────\n";
    for (const auto& p : patches) {
        std::string UP = ToUpper(p.featureName);
        h << "// " << p.className << "::" << p.methodName
          << " → " << p.patchText << "\n"
          << "#define RVA_"      << UP << "  0x" << std::hex << p.rva << std::dec << "ULL\n"
          << "#define LIB_SIZE_" << UP << "  " << p.libSize << "ULL\n"
          << "static const uint8_t bytes_on_"  << p.featureName << "[]  = "
                  << BytesToCArray(p.bytesPatched)  << ";\n"
          << "static const uint8_t bytes_off_" << p.featureName << "[] = "
                  << BytesToCArray(p.bytesOriginal) << ";\n\n";
    }

    // ── Toggle states ────────────────────────────────────────────────────────
    h << "// ── Toggle states ─────────────────────────────────────────────────\n";
    for (const auto& p : patches)
        h << "static bool toggle_" << p.featureName << " = false;\n";
    h << "\n";

    // ── ToggleFeature_ALL ────────────────────────────────────────────────────
    h << "// ── ToggleFeature_ALL: panggil dari mod menu ImGui ────────────────\n"
      << "inline void ToggleFeature_ALL(uintptr_t base) {\n";
    for (const auto& p : patches) {
        std::string UP = ToUpper(p.featureName);
        h << "    PatchMemory(base + RVA_" << UP << ", "
          << "toggle_" << p.featureName << " ? bytes_on_" << p.featureName
          << " : bytes_off_" << p.featureName
          << ", toggle_" << p.featureName << " ? sizeof(bytes_on_" << p.featureName
          << ") : sizeof(bytes_off_" << p.featureName << "));\n";
    }
    h << "}\n\n";

    // ── Contoh DrawModMenu ───────────────────────────────────────────────────
    h << "// ── Contoh DrawModMenu (copy ke mod menu kamu) ────────────────────\n"
      << "// inline void DrawModMenu() {\n"
      << "//     uintptr_t base = GetLibBase(\"libil2cpp.so\");\n";
    for (const auto& p : patches) {
        std::string lbl = p.className + "::" + p.methodName + " [" + p.patchText + "]";
        h << "//     if (ImGui::Checkbox(\"" << lbl << "\", &toggle_"
          << p.featureName << ")) ToggleFeature_ALL(base);\n";
    }
    h << "// }\n";

    return h.str();
}

