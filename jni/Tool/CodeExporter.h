/**
 * @brief Simpan string content ke file.
 *
 * Gunakan ini sebagai ganti Util::FileWriter di semua tempat.
 * Otomatis log sukses/gagal via LOGI/LOGE.
 *
 * @param content   Isi file
 * @param filePath  Path absolut lengkap (misal Util::DirCodegen()+"/file.lua")
 * @return true jika berhasil
 */
inline bool SaveFile(const std::string& content, const std::string& filePath) {
    std::ofstream out(filePath, std::ios::out | std::ios::trunc);
    if (!out.is_open()) {
        LOGE("[CodeExporter] Gagal membuka file: %s", filePath.c_str());
        return false;
    }
    out << content;
    if (out.fail()) {
        LOGE("[CodeExporter] Gagal menulis ke: %s", filePath.c_str());
        return false;
    }
    LOGI("[CodeExporter] Tersimpan: %s (%zu bytes)", filePath.c_str(), content.size());
    return true;
}

