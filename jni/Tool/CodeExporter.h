/// IL2CPP type name → C++ type string
inline std::string TypeToC(const std::string& typeName) {
    if (typeName=="System.Int32"  ||typeName=="Int32")   return "int32_t";
    if (typeName=="System.Int64"  ||typeName=="Int64")   return "int64_t";
    if (typeName=="System.UInt32" ||typeName=="UInt32")  return "uint32_t";
    if (typeName=="System.UInt64" ||typeName=="UInt64")  return "uint64_t";
    if (typeName=="System.Int16"  ||typeName=="Int16")   return "int16_t";
    if (typeName=="System.Boolean"||typeName=="Boolean") return "bool";
    if (typeName=="System.Single" ||typeName=="Single")  return "float";
    if (typeName=="System.Double" ||typeName=="Double")  return "double";
    if (typeName=="System.Byte"   ||typeName=="Byte")    return "uint8_t";
    if (typeName=="System.String" ||typeName=="String")  return "Il2CppString*";
    if (typeName.find("Vector3")    != std::string::npos) return "Vector3";
    if (typeName.find("Vector2")    != std::string::npos) return "Vector2";
    if (typeName.find("Quaternion") != std::string::npos) return "Quaternion";
    return "Il2CppObject*";
}

/// IL2CPP type name → GameGuardian type string
inline std::string TypeToGG(const std::string& typeName) {
    if (typeName=="System.Int32"  ||typeName=="Int32")   return "DWORD";
    if (typeName=="System.Int64"  ||typeName=="Int64")   return "QWORD";
    if (typeName=="System.UInt32" ||typeName=="UInt32")  return "DWORD";
    if (typeName=="System.Single" ||typeName=="Single")  return "FLOAT";
    if (typeName=="System.Boolean"||typeName=="Boolean") return "BYTE";
    if (typeName=="System.Byte"   ||typeName=="Byte")    return "BYTE";
    if (typeName.find("Vector3")  != std::string::npos) return "FLOAT";
    return "DWORD";
}

// ─────────────────────────────────────────────────────────────────────────────
// SECTION 3: ARM64 Patch Byte Derivation  ← SINGLE SOURCE OF TRUTH
//
// BUG FIX: ClassesTab.cpp menggunakan LDR offset 0x18000040/0x58000040/0x1C000040
//          (target register W0/X0/S0 dengan encoding berbeda) sedangkan
//          CodeGenerator.h menggunakan 0x18000060/0x58000060/0x1C000060.
//          Keduanya salah encoding untuk register 0 (W0=0x00, bukan 0x40/0x60).
//
//          Encoding benar ARM64 LDR literal untuk Wn/Xn/Sn dengan register n=0:
//            LDR W0, label  → 0x18000040  (offset 8 byte ke depan dalam instruksi)
//            LDR X0, label  → 0x58000040
//            LDR S0, label  → 0x1C000040
//          Nilai 0x40 = offset +8 dari PC (melewati instruksi LDR itu sendiri + RET).
//          Nilai 0x60 yang dipakai CodeGenerator.h = offset +12, SALAH karena
//          literal pool hanya 8 byte dari LDR.
//
//          Fungsi ini menjadi satu-satunya definisi yang dipakai seluruh proyek.
// ─────────────────────────────────────────────────────────────────────────────

/**
 * @brief Derive ARM64 patch bytes dari return type name + patch text value.
 *
 * Output bytes adalah instruksi ARM64 yang ketika di-execute akan:
 * - Langsung return nilai tertentu (MOV + RET atau LDR literal + RET)
 *
 * @param returnTypeName  Nama tipe kembalian, contoh "System.Boolean", "System.Int32"
 * @param patchValue      Nilai patch sebagai string, contoh "True", "1", "100", "NOP"
 * @return std::vector<uint8_t>  Byte sequence yang siap di-memcpy ke methodPointer
 */
inline std::vector<uint8_t> DerivePatchBytes(
    const std::string& returnTypeName,
    const std::string& patchValue)
{
    constexpr uint32_t RET = 0xD65F03C0;

    // void / NOP → hanya RET
    if (returnTypeName == "System.Void" || patchValue == "NOP") {
        return { 0xC0, 0x03, 0x5F, 0xD6 };
    }

    // bool → MOV W0, #1 ; RET  atau  MOV W0, #0 ; RET
    if (returnTypeName == "System.Boolean") {
        bool v = (patchValue == "True" || patchValue == "true" || patchValue == "1");
        if (v) return { 0x20, 0x00, 0x80, 0x52,   // MOVZ W0, #1
                        0xC0, 0x03, 0x5F, 0xD6 }; // RET
        else   return { 0x00, 0x00, 0x80, 0x52,   // MOVZ W0, #0
                        0xC0, 0x03, 0x5F, 0xD6 }; // RET
    }

    try {
        // int32 / uint32 → LDR W0, [pc+8] ; RET ; .word value
        if (returnTypeName == "System.Int32" || returnTypeName == "System.UInt32") {
            uint32_t v   = (uint32_t)std::stol(patchValue);
            uint32_t ldr = 0x18000040; // LDR W0, 8  (literal pool +8 dari PC)
            std::vector<uint8_t> b(12);
            memcpy(b.data() + 0, &ldr, 4);
            memcpy(b.data() + 4, &RET, 4);
            memcpy(b.data() + 8, &v,   4);
            return b;
        }

        // int64 / uint64 → LDR X0, [pc+8] ; RET ; .quad value
        if (returnTypeName == "System.Int64" || returnTypeName == "System.UInt64") {
            uint64_t v   = (uint64_t)std::stoll(patchValue);
            uint32_t ldr = 0x58000040; // LDR X0, 8
            std::vector<uint8_t> b(16);
            memcpy(b.data() + 0, &ldr, 4);
            memcpy(b.data() + 4, &RET, 4);
            memcpy(b.data() + 8, &v,   8);
            return b;
        }

        // float → LDR S0, [pc+8] ; RET ; .float value
        if (returnTypeName == "System.Single") {
            float    v   = std::stof(patchValue);
            uint32_t ldr = 0x1C000040; // LDR S0, 8
            std::vector<uint8_t> b(12);
            memcpy(b.data() + 0, &ldr, 4);
            memcpy(b.data() + 4, &RET, 4);
            memcpy(b.data() + 8, &v,   4);
            return b;
        }
    }
    catch (...) { /* stol/stof gagal → fallback RET */ }

    // Fallback: hanya RET
    return { 0xC0, 0x03, 0x5F, 0xD6 };
}

