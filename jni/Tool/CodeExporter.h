inline std::string CodeExporter::BuildClassLua(Il2CppClass* klass) {
    if (!klass) return "-- No class selected\n";
    std::ostringstream ss;
    const char* cn   = klass->getName();
    auto fields      = klass->getFields();
    auto methods     = klass->getMethods();
    uintptr_t libBase = GetLibBase(GetTargetLib());

    ss << "-- ============================================================\n"
       << "-- Game Guardian Lua Script\n"
       << "-- Class   : " << (cn ? cn : "?") << "\n"
       << "-- Generated by IL2CPP Tool (CodeExporter)\n"
       << "-- RVA method = offset dari base libil2cpp.so (stabil)\n"
       << "-- ============================================================\n\n";

    // Instance field offsets
    ss << "local offsets = {\n";
    for (auto* f : fields) {
        if (!f || (Il2cpp::GetFieldFlags(f) & 0x10)) continue;
        const char* fn = f->getName(); auto* ft = f->getType();
        ss << "    " << Sanitize(fn ? fn : "unknown") << " = 0x"
           << std::hex << f->getOffset() << std::dec
           << ",  -- " << (ft ? ft->getName() : "?") << "\n";
    }
    ss << "}\n\n";

    // Static field offsets
    bool hasStatic = false;
    for (auto* f : fields) if (f && (Il2cpp::GetFieldFlags(f) & 0x10)) { hasStatic = true; break; }
    if (hasStatic) {
        ss << "local staticOffsets = {\n";
        for (auto* f : fields) {
            if (!f || !(Il2cpp::GetFieldFlags(f) & 0x10)) continue;
            const char* fn = f->getName(); auto* ft = f->getType();
            ss << "    " << Sanitize(fn ? fn : "unknown") << " = 0x"
               << std::hex << f->getOffset() << std::dec
               << ",  -- static " << (ft ? ft->getName() : "?") << "\n";
        }
        ss << "}\n\n";
    }

    // Method RVAs
    if (!methods.empty()) {
        ss << "-- Method RVA — tambahkan ke libBase untuk absolute address\n"
           << "-- local libBase = gg.getRangesList('libil2cpp.so')[1].start\n"
           << "local methodRVA = {\n";
        for (auto* m : methods) {
            if (!m || !m->getName() || !m->methodPointer) continue;
            uintptr_t abs = (uintptr_t)m->methodPointer;
            uintptr_t rva = (libBase && abs > libBase) ? (abs - libBase) : abs;
            ss << "    " << Sanitize(m->getName()) << " = 0x"
               << std::hex << rva << std::dec << ",  -- RVA\n";
        }
        ss << "}\n\n";
    }

    // Read/write helpers
    ss << "local function readField(base, name)\n"
       << "    if not offsets[name] then print('[GG] Field not found: '..name); return nil end\n"
       << "    return gg.getValues({{address=base+offsets[name], type=gg.TYPE_DWORD}})[1].value\n"
       << "end\n\n"
       << "local function writeField(base, name, value, ggType)\n"
       << "    if not offsets[name] then return false end\n"
       << "    ggType = ggType or gg.TYPE_DWORD\n"
       << "    gg.setValues({{address=base+offsets[name], type=ggType, value=value}})\n"
       << "    return true\n"
       << "end\n\n";

    // Per-field setters/getters
    for (auto* f : fields) {
        if (!f || (Il2cpp::GetFieldFlags(f) & 0x10)) continue;
        const char* fn = f->getName(); if (!fn) continue;
        auto* ft = f->getType(); const char* tn = ft ? ft->getName() : "?";
        std::string sn = Sanitize(fn), ggT = TypeToGG(tn ? tn : "?");
        ss << "local function set_" << sn << "(base,v) writeField(base,'" << sn
           << "',v,gg.TYPE_" << ggT << ") end\n"
           << "local function get_" << sn << "(base) return gg.getValues({{address=base+offsets."
           << sn << ",type=gg.TYPE_" << ggT << "}})[1].value end\n\n";
    }

    ss << "local function main()\n"
       << "    print('[GG] " << (cn ? cn : "?") << " loaded OK')\n"
       << "end\n"
       << "main()\n";

    return ss.str();
}

inline std::string CodeExporter::BuildClassCpp(Il2CppClass* klass) {
    if (!klass) return "// No class selected\n";
    std::ostringstream ss;
    const char* cn   = klass->getName();
    std::string safe = Sanitize(cn ? cn : "Unknown");
    auto fields      = klass->getFields();
    auto methods     = klass->getMethods();
    uintptr_t libBase = GetLibBase(GetTargetLib());

    ss << "// ============================================================\n"
       << "// IL2CPP Class Header: " << (cn ? cn : "?") << "\n"
       << "// Generated by IL2CPP Tool (CodeExporter)\n"
       << "// RVA = offset relatif dari GetLibBase(\"libil2cpp.so\")\n"
       << "// ============================================================\n"
       << "#pragma once\n#include <cstdint>\n\n";

    // Detect needed types
    bool needsV3=false, needsV2=false, needsQuat=false, needsStr=false;
    for (auto* f : fields) {
        if (!f) continue; auto* t = f->getType(); if (!t) continue;
        const char* tn = t->getName(); if (!tn) continue;
        if (strstr(tn,"Vector3"))    needsV3   = true;
        if (strstr(tn,"Vector2"))    needsV2   = true;
        if (strstr(tn,"Quaternion")) needsQuat = true;
        if (strstr(tn,"String"))     needsStr  = true;
    }
    if (needsV2)   ss << "struct Vector2 { float x, y; };\n";
    if (needsV3)   ss << "struct Vector3 { float x, y, z; };\n";
    if (needsQuat) ss << "struct Quaternion { float x, y, z, w; };\n";
    if (needsV2 || needsV3 || needsQuat) ss << "\n";
    if (needsStr)  ss << "struct Il2CppString; // Forward declare\n\n";

    // Field offset namespace
    ss << "namespace " << safe << "Offsets {\n";
    for (auto* f : fields) {
        if (!f || (Il2cpp::GetFieldFlags(f) & 0x10)) continue;
        const char* fn = f->getName(); auto* ft = f->getType();
        ss << "    constexpr uintptr_t " << Sanitize(fn ? fn : "unknown")
           << " = 0x" << std::hex << f->getOffset() << std::dec
           << ";  // " << (ft ? ft->getName() : "?") << "\n";
    }
    bool hasStatic = false;
    for (auto* f : fields) if (f && (Il2cpp::GetFieldFlags(f) & 0x10)) { hasStatic = true; break; }
    if (hasStatic) {
        ss << "\n    // Static fields\n";
        for (auto* f : fields) {
            if (!f || !(Il2cpp::GetFieldFlags(f) & 0x10)) continue;
            const char* fn = f->getName(); auto* ft = f->getType();
            ss << "    constexpr uintptr_t " << Sanitize(fn ? fn : "unknown")
               << " = 0x" << std::hex << f->getOffset() << std::dec
               << ";  // static " << (ft ? ft->getName() : "?") << "\n";
        }
    }
    ss << "} // namespace " << safe << "Offsets\n\n";

    // Method RVA namespace
    if (!methods.empty()) {
        ss << "// Method RVA — stabil antar restart game\n"
           << "namespace " << safe << "MethodRVA {\n";
        for (auto* m : methods) {
            if (!m || !m->getName()) continue;
            const char* mn = m->getName();
            auto*    rt    = Il2cpp::GetMethodReturnType(m);
            const char* rn = rt ? Il2cpp::GetTypeName(rt) : "void";
            bool isSt      = Il2cpp::GetIsMethodStatic(m);
            uint32_t pc    = Il2cpp::GetMethodParamCount(m);
            uintptr_t abs  = m->methodPointer ? (uintptr_t)m->methodPointer : 0;
            uintptr_t rva  = (libBase && abs > libBase) ? (abs - libBase) : abs;

            ss << "    // " << (isSt ? "static " : "") << (rn ? rn : "void")
               << " " << mn << "(" << pc << " params)\n";
            if (rva)
                ss << "    constexpr uintptr_t " << Sanitize(mn)
                   << " = 0x" << std::hex << rva << std::dec << ";  // RVA\n";
            else
                ss << "    // constexpr uintptr_t " << Sanitize(mn)
                   << " = 0x0;  // null (abstract/generic)\n";
        }
        ss << "} // namespace " << safe << "MethodRVA\n\n";
    }

    // Struct with padding
    ss << "#pragma pack(push, 1)\n"
       << "struct " << safe << " {\n"
       << "    uintptr_t klass;    // 0x00  Il2CppClass*\n"
       << "    uintptr_t monitor;  // 0x08  monitor\n";

    std::vector<FieldInfo*> instFields;
    for (auto* f : fields) if (f && !(Il2cpp::GetFieldFlags(f) & 0x10)) instFields.push_back(f);
    std::sort(instFields.begin(), instFields.end(),
        [](FieldInfo* a, FieldInfo* b){ return a->getOffset() < b->getOffset(); });

    uintptr_t prevEnd = 0x10; int padIdx = 0; bool firstField = true;
    for (auto* f : instFields) {
        const char* fn = f->getName(); auto* ft = f->getType();
        const char* tn = ft ? ft->getName() : "?";
        uintptr_t offset = f->getOffset();
        std::string ct = TypeToC(tn ? tn : "?");
        size_t sz = 4;
        if (ct=="int64_t"||ct=="uint64_t"||ct=="double"||
            ct=="Il2CppObject*"||ct=="Il2CppString*") sz = 8;
        else if (ct=="int16_t"||ct=="uint16_t") sz = 2;
        else if (ct=="bool"||ct=="uint8_t")     sz = 1;
        else if (ct=="Vector3")    sz = 12;
        else if (ct=="Vector2")    sz = 8;
        else if (ct=="Quaternion") sz = 16;

        if (offset > prevEnd)
            ss << "    char pad_" << padIdx++ << "[" << (offset-prevEnd)
               << "];  // 0x" << std::hex << prevEnd << std::dec << " padding\n";
        else if (offset < prevEnd && !firstField)
            ss << "    // [WARNING] overlap at 0x" << std::hex << offset
               << "  (" << (fn ? fn : "?") << ")\n" << std::dec;
        firstField = false;
        ss << "    " << ct << " " << Sanitize(fn ? fn : "unknown")
           << ";  // 0x" << std::hex << offset << std::dec
           << "  " << (tn ? tn : "?") << "\n";
        prevEnd = offset + sz;
    }
    ss << "};\n#pragma pack(pop)\n\n";

    ss << "#define READ_"  << safe << "(base,field) "
       << "(*reinterpret_cast<decltype(" << safe << "::field)*>"
       << "((uintptr_t)(base)+" << safe << "Offsets::field))\n"
       << "#define WRITE_" << safe << "(base,field,val) "
       << "READ_" << safe << "(base,field)=(val)\n";

    return ss.str();
}
