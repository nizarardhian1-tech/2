/// Byte array → "AA BB CC" (spaced hex, untuk GG Lua hexON/hexOFF)
inline std::string BytesToSpacedHex(const std::vector<uint8_t>& b) {
    std::ostringstream ss;
    for (size_t i = 0; i < b.size(); i++) {
        char h[4]; snprintf(h, sizeof(h), "%02X", b[i]);
        ss << h;
        if (i + 1 < b.size()) ss << " ";
    }
    return ss.str();
}

/// Byte array → "{0xAA,0xBB,0xCC}" (C array literal, untuk AutoGenerated.h)
inline std::string BytesToCArray(const std::vector<uint8_t>& b) {
    std::ostringstream ss;
    ss << "{";
    for (size_t i = 0; i < b.size(); i++) {
        char h[6]; snprintf(h, sizeof(h), "0x%02X", b[i]);
        ss << h;
        if (i + 1 < b.size()) ss << ",";
    }
    ss << "}";
    return ss.str();
}

/// String → UPPERCASE
inline std::string ToUpper(std::string s) {
    std::transform(s.begin(), s.end(), s.begin(), ::toupper);
    return s;
}

/// String → identifier C++ yang valid (non-alnum/_ diganti '_')
inline std::string Sanitize(const std::string& s) {
    std::string out;
    out.reserve(s.size());
    for (char c : s) {
        out += (std::isalnum((unsigned char)c) || c == '_') ? c : '_';
    }
    if (!out.empty() && std::isdigit((unsigned char)out[0]))
        out = "_" + out;
    return out;
}

