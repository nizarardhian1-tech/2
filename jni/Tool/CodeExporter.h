/// Build isi file ModMenu_GG.lua dari list patches
inline std::string BuildLuaMenu(const std::vector<CGPatchEntry>& patches) {
    std::ostringstream lua;

    lua << "-- ============================================================\n"
        << "-- Game Guardian Mod Menu\n"
        << "-- Auto Generated by IL2CPP Tool (CodeExporter)\n"
        << "-- RVA = Offset dari base libil2cpp.so (stabil antar restart)\n"
        << "-- ============================================================\n\n";

    // ── FEATURES table ───────────────────────────────────────────────────────
    lua << "local FEATURES = {\n";
    for (size_t i = 0; i < patches.size(); i++) {
        const auto& p = patches[i];
        size_t mb = p.libSize / (1024 * 1024);
        size_t kb = (p.libSize % (1024 * 1024)) / 1024;
        lua << "    [" << (i + 1) << "] = {\n"
            << "        name    = \"" << p.className << "::" << p.methodName
                                     << " [" << p.patchText << "]\",\n"
            << "        rva     = 0x" << std::hex << p.rva << std::dec << ",\n"
            << "        lib     = \"" << p.libName << "\",\n"
            << "        libSize = " << p.libSize
                                    << ", -- " << mb << "." << kb << " MB\n"
            << "        hexON   = \"" << BytesToSpacedHex(p.bytesPatched)  << "\",\n"
            << "        hexOFF  = \"" << BytesToSpacedHex(p.bytesOriginal) << "\",\n"
            << "        enabled = false,\n"
            << "    },\n";
    }
    lua << "}\n\n";

    // ── Base finder dengan region-size filter ────────────────────────────────
    lua << "-- Cache base address agar tidak scan berulang\n"
        << "local _baseCache = {}\n"
        << "local function getBase(libName, libSize)\n"
        << "    local key = libName .. '_' .. tostring(libSize)\n"
        << "    if _baseCache[key] then return _baseCache[key] end\n"
        << "    local ranges = gg.getRangesList(libName)\n"
        << "    if not ranges or #ranges == 0 then\n"
        << "        gg.alert('\\xE2\\x9D\\x8C Library tidak ditemukan: ' .. libName)\n"
        << "        return nil\n"
        << "    end\n"
        << "    -- Sort by size descending\n"
        << "    table.sort(ranges, function(a,b)\n"
        << "        return (a['end']-a.start) > (b['end']-b.start)\n"
        << "    end)\n"
        << "    -- Jika libSize valid: cari exact match dulu\n"
        << "    if libSize and libSize > 0 then\n"
        << "        local targetMB = math.floor(libSize / 1048576)\n"
        << "        for _, r in ipairs(ranges) do\n"
        << "            if (r['end'] - r.start) == libSize then\n"
        << "                _baseCache[key] = r.start; return r.start\n"
        << "            end\n"
        << "        end\n"
        << "        -- MB-level match\n"
        << "        for _, r in ipairs(ranges) do\n"
        << "            local szMB = math.floor((r['end'] - r.start) / 1048576)\n"
        << "            if szMB == targetMB and targetMB > 0 then\n"
        << "                _baseCache[key] = r.start; return r.start\n"
        << "            end\n"
        << "        end\n"
        << "    end\n"
        << "    -- Fallback: jika libSize = 0 atau tidak ada match,\n"
        << "    -- pakai region terbesar ke-2 (untuk split APK multi-region).\n"
        << "    -- Region terbesar biasanya libunity.so atau region utama yg bukan target.\n"
        << "    local idx = (#ranges >= 2) and 2 or 1\n"
        << "    local r = ranges[idx]\n"
        << "    local sz = math.floor((r['end'] - r.start) / 1048576)\n"
        << "    gg.toast('\\xE2\\x9A\\xA0 getBase fallback: region #' .. idx .. ' (' .. sz .. ' MB)')\n"
        << "    _baseCache[key] = r.start\n"
        << "    return r.start\n"
        << "end\n\n";

    // ── Patch writer ─────────────────────────────────────────────────────────
    lua << "local function writePatch(base, rva, hexStr)\n"
        << "    if not base then return false end\n"
        << "    local addr = base + rva\n"
        << "    local t, i = {}, 0\n"
        << "    for h in string.gmatch(hexStr, '%S%S') do\n"
        << "        table.insert(t, {address = addr + i, flags = gg.TYPE_BYTE, value = h .. 'r'})\n"
        << "        i = i + 1\n"
        << "    end\n"
        << "    return gg.setValues(t) ~= nil\n"
        << "end\n\n";

    // ── Toggle ON / OFF ──────────────────────────────────────────────────────
    lua << "local function featureON(f)\n"
        << "    local base = getBase(f.lib, f.libSize)\n"
        << "    if writePatch(base, f.rva, f.hexON) then\n"
        << "        f.enabled = true\n"
        << "        gg.toast('\\xE2\\x9C\\x85 ON: ' .. f.name)\n"
        << "    end\n"
        << "end\n\n"
        << "local function featureOFF(f)\n"
        << "    local base = getBase(f.lib, f.libSize)\n"
        << "    if writePatch(base, f.rva, f.hexOFF) then\n"
        << "        f.enabled = false\n"
        << "        gg.toast('\\xE2\\x9D\\x8C OFF: ' .. f.name)\n"
        << "    end\n"
        << "end\n\n";

    // ── Menu builder ─────────────────────────────────────────────────────────
    lua << "local function buildMenu()\n"
        << "    local items = {}\n"
        << "    for _, f in ipairs(FEATURES) do\n"
        << "        local icon = f.enabled and '\\xE2\\x9C\\x85 ON  ' or '\\xE2\\x9D\\x8C OFF '\n"
        << "        table.insert(items, icon .. f.name)\n"
        << "    end\n"
        << "    table.insert(items, '\\xE2\\x9E\\x95 Semua ON')\n"
        << "    table.insert(items, '\\xE2\\x9E\\x96 Semua OFF')\n"
        << "    table.insert(items, '\\xF0\\x9F\\x9A\\xAA Keluar')\n"
        << "    return items\n"
        << "end\n\n";

    // ── Main loop ────────────────────────────────────────────────────────────
    lua << "local running = true\n"
        << "while running do\n"
        << "    local n      = #FEATURES\n"
        << "    local choice = gg.choice(buildMenu(), nil, 'IL2CPP Mod Menu | ' .. n .. ' Fitur')\n"
        << "    if not choice then\n"
        << "        running = false\n"
        << "    elseif choice <= n then\n"
        << "        local f = FEATURES[choice]\n"
        << "        if f.enabled then featureOFF(f) else featureON(f) end\n"
        << "    elseif choice == n + 1 then\n"
        << "        for _, f in ipairs(FEATURES) do featureON(f) end\n"
        << "    elseif choice == n + 2 then\n"
        << "        for _, f in ipairs(FEATURES) do featureOFF(f) end\n"
        << "    else\n"
        << "        running = false\n"
        << "    end\n"
        << "    gg.sleep(150)\n"
        << "end\n"
        << "print('[ModMenu] Selesai.')\n";

    return lua.str();
}

