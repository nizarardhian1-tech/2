/// Hitung ukuran region yang benar untuk sebuah library.
/// Bekerja untuk lib di-extract normal MAUPUN lib embedded di split APK.
inline size_t GetLibRegionSize(const char* libName) {
    uintptr_t base = GetLibBase(libName); // libil2cpp.so → il2cpp_base

    FILE* fp = fopen("/proc/self/maps", "r");
    if (!fp) return 0;
    char line[512];

    // Pass 1: cari entry /proc/self/maps yang contain base address
    // Ini works untuk split APK (path entry = path APK, bukan libil2cpp.so)
    char targetPath[256] = {};
    if (base) {
        while (fgets(line, sizeof(line), fp)) {
            uintptr_t s = 0, e = 0;
            sscanf(line, "%lx-%lx", &s, &e);
            if (s <= base && base < e) {
                char perms[8]={}, dev[16]={}, inode_s[32]={}, path[256]={};
                unsigned long long off = 0;
                int n = sscanf(line, "%*lx-%*lx %7s %llx %15s %31s %255[^\n]",
                               perms, &off, dev, inode_s, path);
                if (n >= 5 && path[0])
                    strncpy(targetPath, path, sizeof(targetPath)-1);
                break;
            }
        }
    }

    // Pass 2: jika ketemu path file, sum semua region untuk file itu
    if (targetPath[0]) {
        rewind(fp);
        uintptr_t firstStart = 0, lastEnd = 0;
        while (fgets(line, sizeof(line), fp)) {
            if (!strstr(line, targetPath)) continue;
            uintptr_t s = 0, e = 0;
            sscanf(line, "%lx-%lx", &s, &e);
            if (!firstStart || s < firstStart) firstStart = s;
            if (e > lastEnd) lastEnd = e;
        }
        fclose(fp);
        if (firstStart && lastEnd > firstStart)
            return lastEnd - firstStart;
    }

    // Fallback: cari by nama libName (lib di-extract ke disk normal)
    rewind(fp);
    uintptr_t firstStart = 0, lastEnd = 0;
    while (fgets(line, sizeof(line), fp)) {
        if (!strstr(line, libName)) continue;
        uintptr_t s = 0, e = 0;
        sscanf(line, "%lx-%lx", &s, &e);
        if (!firstStart || s < firstStart) firstStart = s;
        if (e > lastEnd) lastEnd = e;
    }
    fclose(fp);
    return (firstStart && lastEnd > firstStart) ? (lastEnd - firstStart) : 0;
}

