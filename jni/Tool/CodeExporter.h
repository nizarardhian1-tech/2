/**
 * @brief Export semua patch aktif ke ModMenu_GG.lua + AutoGenerated.h
 *
 * Ini adalah entry point utama yang dipanggil dari:
 *   - ClassesTab::DrawPatchManager()
 *   - CodeGeneratorUI (jika diinginkan)
 *
 * @tparam OMapType  unordered_map<MethodInfo*, T>  (T harus punya .bytes & .text)
 * @param  oMap      Map patch aktif
 */
template <typename OMapType>
inline void ExportAll(const OMapType& oMap) {
    auto patches = CollectPatches(oMap);
    if (patches.empty()) {
        LOGE("[CodeExporter] ExportAll: tidak ada patch aktif untuk diekspor.");
        return;
    }

    const std::string dir = Util::DirCodegen();

    // 1. Lua Menu
    {
        const std::string path = dir + "/ModMenu_GG.lua";
        SaveFile(BuildLuaMenu(patches), path);
    }

    // 2. C++ Header
    {
        const std::string path = dir + "/AutoGenerated.h";
        SaveFile(BuildCppHeader(patches), path);
    }

    LOGI("[CodeExporter] ExportAll selesai: %zu patch → %s/", patches.size(), dir.c_str());
}

